package counter

// [5.1][5.3] НАМЕРЕННО НЕБЕЗОПАСНАЯ ВЕРСИЯ (для демонстрации -race)
// ❌ Не используй этот код в проде! Только для обучения.

type UnsafeCounter struct {
	count int64 // [5.1] int64 для атомарных операций, но без sync — это гонка
}

// Increment увеличивает счётчик.
// ⚠️ ПРОБЛЕМА: чтение-изменение-запись не атомарно.
// Два goroutine могут прочитать одно значение, увеличить и записать → потеря обновления.
// 📖 Изучить: https://go.dev/ref/mem (Go Memory Model)
func (c *UnsafeCounter) Increment() {
	c.count++ // DATA RACE: не атомарная операция!
}

// Value возвращает текущее значение.
// ⚠️ ПРОБЛЕМА: чтение без синхронизации может увидеть «полуприсвоенное» значение.
func (c *UnsafeCounter) Value() int64 {
	return c.count
}
